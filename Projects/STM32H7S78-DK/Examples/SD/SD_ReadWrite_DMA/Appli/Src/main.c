/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
  uint32_t index = 0;
  HAL_SD_CardCIDTypeDef pCID;
  HAL_SD_CardCSDTypeDef pCSD;

  MPU_Config();
  /* USER CODE END 1 */

  /* Enable the CPU Cache */

  /* Enable I-Cache---------------------------------------------------------*/
  SCB_EnableICache();

  /* Enable D-Cache---------------------------------------------------------*/
  SCB_DisableDCache();

  /* MCU Configuration--------------------------------------------------------*/

  /* Update SystemCoreClock variable according to RCC registers values. */
  SystemCoreClockUpdate();

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SDMMC1_SD_Init();
  /* USER CODE BEGIN 2 */

  /* Switch to High Speed mode if the card support this mode */
  HAL_SD_ConfigSpeedBusOperation(&hsd1,SDMMC_SPEED_MODE_HIGH);


  if(HAL_SD_Erase(&hsd1, ADDRESS, ADDRESS+BUFFER_SIZE) != HAL_OK)
  {
    Error_Handler();
  }
  if(Wait_SDCARD_Ready() != HAL_OK)
  {
    Error_Handler();
  }

  HAL_SD_GetCardCID(&hsd1, &pCID);
  HAL_SD_GetCardCSD(&hsd1, &pCSD);

  {
    /*##- 2 - Initialize Transmission buffer #####################*/
    for (index = 0; index < BUFFER_SIZE; index++)
    {
      aTxBuffer[index] = DATA_PATTERN + index;
    }

  }

  {
    TxCplt = 0;
    /*##- 3 - Start Transmission buffer #####################*/
    if(HAL_SD_WriteBlocks_DMA(&hsd1, aTxBuffer, ADDRESS, NB_BLOCK_BUFFER) != HAL_OK)
    {
      Error_Handler();
    }
    while(TxCplt == 0);
  }

  {
    /*##- 4 - Initialize Reception buffer #####################*/
    for (index = 0; index < BUFFER_SIZE; index++)
    {
      aRxBuffer[index] = 0;
    }

  }

  {
    /*##- 5 -  Data read  #####################*/
    RxCplt = 0;
    if(HAL_SD_ReadBlocks_DMA(&hsd1, aRxBuffer, ADDRESS, NB_BLOCK_BUFFER) != HAL_OK)
    {
      Error_Handler();
    }
    while(RxCplt == 0);
  }

  {

    /*##- 6 - Check Reception buffer #####################*/

    for (index = 0 ; index< (BUFFER_SIZE); index++)
    {
      if (aRxBuffer[index] != aTxBuffer[index])
      {
        BSP_LED_On(LED_RED);
        break;
      }
    }
    if (index == BUFFER_SIZE )
    {
      BSP_LED_On(LED_GREEN);
    }
    
  }

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

