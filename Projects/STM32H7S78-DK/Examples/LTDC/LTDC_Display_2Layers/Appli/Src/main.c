  MX_USART1_UART_Init();
  /* USER CODE BEGIN 2 */
  
    /*##-2- CLUT Configuration #################################################*/
  HAL_LTDC_ConfigCLUT(&hltdc, (uint32_t *)L8_320x240_CLUT, 256, 0); 
  
  /*##-3- Enable CLUT For Layer 1 ############################################*/
  HAL_LTDC_EnableCLUT(&hltdc, 0);   

