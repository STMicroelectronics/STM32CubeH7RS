  if(HAL_USART_TransmitReceive(&husart1, (uint8_t*)aTxBuffer, (uint8_t*)aRxBuffer, TXBUFFERSIZE, 0x1FFFFFF)!= HAL_OK)
  {
    Error_Handler();
  }

